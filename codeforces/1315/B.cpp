string s;
int a, b, p;

bool can(int idx) {
  int n = s.length();
  //dbg(a, b, p, n);
  ll ret = 0;
  char last = 'M';
  for (int i = idx; i < n - 1; i++) {
    if (s[i] == last)continue;
    if (s[i] == 'A') ret += a;
    else ret += b;
    last = s[i];
  }
  return p >= ret;
}

